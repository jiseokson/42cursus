int	parse_int(char *str, int lo, int hi)
{
	int	ret;
	int	i;

	ret = 0;
	i = 0;
	while (lo + i <= hi)
	{
		if (! ('0' <= str[lo + i] && str[lo + i] <= '9'))
			return (-1);
		ret *= 10;
		ret += str[lo + i] - '0';
		++i;
	}
	return (ret);
}
