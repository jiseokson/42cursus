void	lower(char *c)
{
	if ('A' <= *c && *c <= 'Z')
		*c = *c - 'A' + 'a';
}

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		lower(&str[i++]);
	return (str);
}
