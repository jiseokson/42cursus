void	upper(char *c)
{
	if ('a' <= *c && *c <= 'z')
		*c = *c - 'a' + 'A';
}

char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		upper(&str[i++]);
	return (str);
}
